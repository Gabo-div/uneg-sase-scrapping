export const semesterWithSuffix = (num: number) => {
  if (num === 1) return num + "ro";

  if (num === 2) return num + "do";

  if (num === 3) return num + "ro";

  if (num === 4) return num + "to";

  if (num === 5) return num + "to";

  if (num === 6) return num + "to";

  if (num === 7) return num + "mo";

  if (num === 8) return num + "vo";

  if (num === 9) return num + "no";
};
